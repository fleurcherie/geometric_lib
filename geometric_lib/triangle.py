def triangle_area(a, h):
    '''
    Вычисляет площадь треугольника по основанию и высоте.

    Параметры:
        a (float): основание треугольника
        h (float): высота, проведённая к основанию a

    Возвращаемое значение:
        float: площадь треугольника
    Пример:
        >>> triangle_area(6, 4)
        12.0
    '''
    return a * h / 2


def triangle_perimeter(a, b, c):
    '''
    Вычисляет периметр треугольника по трём сторонам.

    Параметры:
        a (float): первая сторона
        b (float): вторая сторона
        c (float): третья сторона
    Возвращаемое значение:
        float: периметр треугольника
    Пример:
        >>> triangle_perimeter(3, 4, 5)
        12
    '''
    return a + b + c